function [fitresult, gof] = DriftFit2(time, fitvar)
%DRIFTFIT2 Takes input points over time and corrects them for drift using a
%linear fit. 
%   Looks at positions in time to determine the rate of drift and then
%   returns a linear fit with a slope representing the drift that can then
%   be solved to remove drift from each original point. simple y=mx+b
%   polynomial fit.

%CREATEFIT(TIME,X_T)
%  Create a fit.
%
%  Data for 'untitled fit 1' fit:
%      X Input : time
%      Y Output: X_T
%  Output:
%      fitresult : a fit object representing the fit.
%      gof : structure with goodness-of fit info.
%
%  See also FIT, CFIT, SFIT.

%  Auto-generated by MATLAB on 16-Nov-2020 11:17:03


%% Create Fit:
[xData, yData] = prepareCurveData( time, fitvar );

% Set up fittype and options.
ft = fittype( 'poly1' );

% Fit model to data.
[fitresult, gof] = fit( xData, yData, ft );

end

